<script type="text/javascript">
    function showMessage() {
        swal({
            title: "Student Registration",
            text: "You have successfully Registered.",
            type: "success",
            showCancelButton: true,
            confirmButtonText: "Admin Message",
            cancelButtonText: "Cancel",
            closeOnConfirm: false,
            closeOnCancel: false
        }, function(isConfirm) {
            if (isConfirm) {
                swal("Admin Message", "Please wait for admin accepted you.Admin send mail.", "success");
            }
        });
    }
</script>
<script>

    $("#isAccept").change(function(){
        if ($(this).is(':checked'))
        {
            $( "#registration_btn" ).prop( "disabled", false );
        }else
        {
            $( "#registration_btn" ).prop( "disabled", true );
        }
    });
    $( "#emailId" ).change(function() {
       var emailId = $("#emailId").val();
        $.ajax(
            {
                method:'POST',
                data:{email:emailId},
                url:'check_registration_email',
                dataType: 'json',
                success:function (data) {
                    if (data == 1)
                    {
                        $('.email').text('This Email Already Exist').css('color','red');
                        $( "#registration_btn" ).prop( "disabled", true );
                    }else {
                        $('.email').text('').css('color','red');
                        $( "#registration_btn" ).prop( "disabled", true );
                    }
                }
            });
    });
    $("#reset_btn").click(function () {
        $('#std_registration_form')[0].reset();
    });
    $('#registration_btn').click(function () {

        var student_info = $('#std_registration_form').serialize();
        $.ajax(
            {
                method:'POST',
                data:student_info,
                url:'registration',
                dataType: 'json',
                success:function (data) {
                    if (data.error_list)
                    {
                        $.each(data.error_list, function(key,val) {
                            $('.'+key).text(val).css('color','red');
                        });
                    }else
                    {
                        showMessage();
                        $('#std_registration_form')[0].reset();
                    }
                }
            });

    });</script>